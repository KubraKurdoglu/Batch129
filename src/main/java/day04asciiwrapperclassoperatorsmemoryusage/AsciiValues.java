package day04asciiwrapperclassoperatorsmemoryusage;

public class AsciiValues {

    // java da her karakterin sayisal bir degeri vardir.
    // bu degerler ASCII degerler olarzak adlandirilir
    // bu degerlerin tamaminin bulundugu tabloya ASCII table denir


    public static void main(String[] args) {

        // herhangi bir karakterin ASCII degrini bulmak icin o karakteri
        // int data type sinde bir variable nin icine koyarsaniz bulunur

        int unlem= '!';
        System.out.println(unlem);

        char c1 = 'k';
        char c2 = '?';
        // java da char lari matematiksel islemlerde kullanirsaniz java o char'larin ASCII dgerlerini kullanir

        System.out.println(c1+c2);





    }
}
