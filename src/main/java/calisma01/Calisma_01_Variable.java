package calisma01;

public class Calisma_01_Variable {

    public static void main(String[] args) {

        //Example 1: ogrenci ismi variable olusturup deger olarak Ali Can atayiniz

        String studentName = "Ali Can";
        System.out.println(studentName);

        //Example 2; char data type sinde ilk ismin ilk harfi oalrak bir variable olusturunuz
        // ve bir deger atayiniz.

        char premierLettre = 'a';
        System.out.println(premierLettre);


        //example 3: boolean data type sinde "emekli misin" diye bir variable olusturup ve false
        //degeri atayin

        boolean emeklimisin =false;
        System.out.println(emeklimisin);


        // example 4: byte data type sinde ogrenci yasi icin bir variable olusturunuz ve
        // deger atayiniz

        byte studentAge = 13;
        System.out.println(studentAge);


        //example 5; short data type sinde site nufusu icin bir variable olustrunuz ve dger atayiniz

        short populationSite = 12200;
        System.out.println(populationSite);

        //Example 6: ulke nufusu icin variable olusturun ve bir deger atayin

        int populationPays = 2453654;
        System.out.println(populationPays);

        // Example 7: long data type sinde insan hucre sayisi cin variable olustur
        //deger atamasi yapiniz

        long cellNumerInHumanBody = 345672457788912L;
        System.out.println(cellNumerInHumanBody);

        //sen long desen de bu integer diyor java, sonra integer bu kadar uzun olamaz deyip
        //hata veriyor, bizde long da israrci olmak icin sonuna "L" koyuyoruz













    }
}
